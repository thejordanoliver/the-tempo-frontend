export const getSOCCTeam = (id: number | string) => {
  if (id == null) return undefined;
  return soccerTeams.find((t) => String(t.id) === String(id));
};

export function getSOCCTeamLogo(
  id: number | string | undefined,
  isDark: boolean,
) {
  if (!id) return PlaceholderLogo;

  const team = soccerTeams.find((t) => String(t.id) === String(id));

  return team
    ? isDark
      ? team.logoLight || team.logo
      : team.logo
    : PlaceholderLogo;
}
