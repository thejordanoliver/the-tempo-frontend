export const getCBTeam = (id: number | string) => {
  if (id == null) return undefined;
  return cbTeams.find((t) => String(t.id) === String(id));
};

export function getCBTeamLogo(
  id: number | string | undefined,
  isDark: boolean,
) {
  const team = cbTeams.find((t) => String(t.id) === String(id));
  if (!id || team?.logo === null) return PlaceholderLogo;

  return team
    ? isDark
      ? team.logoLight || team.logo
      : team.logo
    : PlaceholderLogo;
}
