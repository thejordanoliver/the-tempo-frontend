export const getSBTeam = (id: number | string) => {
  if (id == null) return undefined;
  return sbTeams.find((t) => String(t.id) === String(id));
};

export function getSBTeamLogo(
  id: number | string | undefined,
  isDark: boolean,
) {
  const team = sbTeams.find((t) => String(t.id) === String(id));
  if (!id || team?.logo === null) return PlaceholderLogo;

  return team
    ? isDark
      ? team.logoLight || team.logo
      : team.logo
    : PlaceholderLogo;
}
